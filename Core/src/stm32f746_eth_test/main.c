// 送封包測試
void ETH_TestTx(void)
{
    ETH_TxPacketConfigTypeDef TxConfig;
    ETH_BufferTypeDef TxBuffer;
    char buf[64];

    // 選擇下一個 TX buffer
    static uint32_t tx_index = 0;
    uint8_t* testFrame = Tx_Buff[tx_index];
    tx_index = (tx_index + 1) % ETH_TX_DESC_CNT;

    // 準備封包
    prepareTestFrame(testFrame);

    // 清除 D-Cache
    SCB_CleanDCache_by_Addr((uint32_t*)testFrame, ETH_TX_BUF_SIZE);

    // 填 TxBuffer
    TxBuffer.buffer = testFrame;
    TxBuffer.len    = 64;
    TxBuffer.next   = NULL;

    // 填 TxConfig
    memset(&TxConfig, 0, sizeof(TxConfig));
    TxConfig.Attributes = ETH_TX_PACKETS_FEATURES_CSUM | ETH_TX_PACKETS_FEATURES_CRCPAD;
    TxConfig.ChecksumCtrl = ETH_CHECKSUM_DISABLE;
    TxConfig.CRCPadCtrl = ETH_CRC_PAD_INSERT;
    TxConfig.Length       = 64;
    TxConfig.TxBuffer     = &TxBuffer;

    UART_SendString("TX start...\r\n");
    HAL_StatusTypeDef ret = HAL_ETH_Transmit(&heth, &TxConfig, 1000);

    if (ret == HAL_OK) {
        UART_SendString("TX OK!\r\n");
    } else {
        mini_snprintf(buf, sizeof(buf), "ErrorCode=0x%x, DMAErrorCode=0x%x\r\n",
                      heth.ErrorCode, heth.DMAErrorCode);
        UART_SendString(buf);
        UART_SendString("TX FAIL!\r\n");
    }

    HAL_ETH_ReleaseTxPacket(&heth);
}

void ETH_TestRx(void)
{
    uint8_t *pBuff = NULL;   // 讓 HAL_ETH_ReadData 填進 buffer 位址
    char buf[64];

    HAL_StatusTypeDef HalStatus = HAL_ETH_ReadData(&heth, (void**)&pBuff);

    if (HalStatus == HAL_OK && pBuff != NULL) {
        UART_SendString("RX OK, dump first 100 bytes:\r\n");

        uint32_t dump_len = 100;
        for (uint32_t i = 0; i < dump_len; i++) {
            mini_snprintf(buf, sizeof(buf), "%2x ", pBuff[i]);
            UART_SendString(buf);
            if ((i + 1) % 16 == 0) UART_SendString("\r\n"); // 每 16 bytes 換行
        }
        UART_SendString("\r\n");
    } else {
        mini_snprintf(buf, sizeof(buf), "RX FAIL or no data, error=%d\r\n", HalStatus);
        UART_SendString(buf);
    }
}


